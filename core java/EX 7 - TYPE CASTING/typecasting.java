public class TypeCasting {
    public static void main(String[] args) {

        double d = 10.75;
        int i = (int)d;

        System.out.println("Double: " + d);
        System.out.println("Int: " + i);

        int num = 25;
        double value = num;

        System.out.println("Integer: " + num);
        System.out.println("Double: " + value);
    }
}