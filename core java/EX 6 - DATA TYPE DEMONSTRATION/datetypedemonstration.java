public class DataTypeDemo {
    public static void main(String[] args) {

        int num = 100;
        float f = 10.5f;
        double d = 99.99;
        char ch = 'A';
        boolean flag = true;

        System.out.println("int: " + num);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + ch);
        System.out.println("boolean: " + flag);
    }
}